const mongoose = require("mongoose");

// Create a mongoose schema
const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

// Create a mongoose model
const User = mongoose.model("user", UserSchema);

module.exports = User;
